package com.wheelAround.model;

public class Vehicles {
private String typeId;
private String VID;
private String maintainance_by;
private String ownerId;
private String garageId;
private String modelName;
public String getTypeId() {
	return typeId;
}
public void setTypeId(String typeId) {
	this.typeId = typeId;
}
public String getVID() {
	return VID;
}
public void setVID(String vID) {
	VID = vID;
}
public String getMaintainance_by() {
	return maintainance_by;
}
public void setMaintainance_by(String maintainance_by) {
	this.maintainance_by = maintainance_by;
}
public String getOwnerId() {
	return ownerId;
}
public void setOwnerId(String ownerId) {
	this.ownerId = ownerId;
}
public String getGarageId() {
	return garageId;
}
public void setGarageId(String garageId) {
	this.garageId = garageId;
}
public String getModelName() {
	return modelName;
}
public void setModelName(String modelName) {
	this.modelName = modelName;
}

}
